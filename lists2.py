def multiplyList(myList):
    # Multiply elements one by one
    result = 1
    for x in myList:
        result = result * x
    return result

lists1 = [1, 2, 3]
lists2 = [3, 2, 4]
print(multiplyList(list1))
print(multiplyList(list2))
